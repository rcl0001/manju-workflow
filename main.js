/**
 * 运行完整漫剧生成工作流
 * @param {string} novelText 输入的小说文本
 * @returns {string} 最终生成的漫剧视频本地路径
 */
export const runManjuWorkflow = async (novelText) => {
  console.log("🚀 开始漫剧生成全链路...");
  
  // ------------------------------
  // Step1：生成结构化剧本
  // ------------------------------
  console.log("\n📝 Step1/5：生成结构化剧本");
  const fullScript = await 剧本生成Agent(novelText);
  // 拆分完整剧本为三个字段：角色信息、环境信息、剧本内容
  const [roleInfoPart, envInfoPart, scriptContentPart] = splitScript(fullScript);
  // 保存中间结果到本地
  await writeJsonFile(
    { roleInfo: roleInfoPart, envInfo: envInfoPart, scriptContent: scriptContentPart },
    './assets/step1_script.json'
  );

  // ------------------------------
  // Step2：生成角色三视图
  // ------------------------------
  console.log("\n🎨 Step2/5：生成角色三视图");
  const charRefs = await 角色三视图生成Agent(roleInfoPart);
  // 保存中间结果到本地
  await writeJsonFile(charRefs, './assets/step2_char_refs.json');

  // ------------------------------
  // Step3：生成环境参考图
  // ------------------------------
  console.log("\n🏞️ Step3/5：生成环境参考图");
  const sceneRefs = await 环境参考图生成Agent(envInfoPart);
  // 保存中间结果到本地
  await writeJsonFile(sceneRefs, './assets/step3_scene_refs.json');

  // ------------------------------
  // Step4：生成视频提示词
  // ------------------------------
  console.log("\n✍️ Step4/5：生成视频提示词");
  const videoPrompts = await 视频提示词生成Agent(scriptContentPart, charRefs, sceneRefs);
  // 保存中间结果到本地
  await writeJsonFile(videoPrompts, './assets/step4_video_prompts.json');

  // ------------------------------
  // Step5：生成最终漫剧视频
  // ------------------------------
  console.log("\n🎬 Step5/5：生成最终漫剧视频");
  const finalVideoPath = await 视频生成Agent(videoPrompts);

  console.log(`\n🎉 漫剧生成完成！最终视频路径：${finalVideoPath}`);
  console.log("所有中间产物已保存到assets目录下");
  return finalVideoPath;
};

/**
 * 拆分完整剧本为三个独立字段：角色信息、环境信息、剧本内容
 * @param {string} fullScript 完整的剧本文本
 * @returns {Array} [角色信息文本, 环境信息文本, 剧本内容文本]
 */
const splitScript = (fullScript) => {
  // 按Markdown标题拆分剧本
  const parts = fullScript.split('### ');
  // 提取对应部分的内容，去掉标题本身
  const roleInfoPart = (parts.find(p => p.startsWith('角色信息')) || '').replace('角色信息\n', '').trim();
  const envInfoPart = (parts.find(p => p.startsWith('环境信息')) || '').replace('环境信息\n', '').trim();
  const scriptContentPart = (parts.find(p => p.startsWith('剧本内容')) || '').replace('剧本内容\n', '').trim();
  return [roleInfoPart, envInfoPart, scriptContentPart];
};

